// One-off Neon -> D1 data migration.
//
// Reads every public table from Neon and emits a single SQL file of
// INSERTs, then applies it to the remote D1 database in one round trip.
// Column-type adaptation per the d1-driver conventions:
//   * boolean   -> 0/1
//   * Date / ISO timestamps -> ISO string TEXT
//   * int[] / pg arrays     -> JSON string TEXT
//   * uuids     -> pass through as TEXT
//
// Run order respects foreign keys (parents first). Idempotent on the
// emitted .sql file via `ON CONFLICT DO NOTHING` so a partial apply
// doesn't half-corrupt the target.

import { neon } from '@neondatabase/serverless';
import { execFileSync } from 'node:child_process';
import { writeFileSync, readFileSync } from 'node:fs';

const DATABASE_URL = readFileSync('.dev.vars', 'utf8').match(/DATABASE_URL=(.+)/)[1].trim();
const sql = neon(DATABASE_URL);

// Foreign-key respecting order.
const TABLE_ORDER = [
  'users',
  'sites',
  'site_members',
  'site_domain_history',
  'tags',
  'images',
  'posts',
  'post_tags',
  'sessions',
  'pages',
  'albums',
  'album_images',
];

// Columns to convert per table. Anything not listed passes through as
// the JSON value Neon returned. Postgres-arrays come back as native JS
// arrays via the Neon driver; ISO timestamps come back as JS Date objects
// or pre-formatted strings depending on column type — both round-trip
// through new Date().toISOString().
const BOOL_COLS = {
  posts: [],
  albums: ['published'],
  images: ['has_avif'],
};
const ARRAY_JSON_COLS = {
  images: ['variant_widths'],
};

function escapeSqlLiteral(v) {
  if (v === null || v === undefined) return 'NULL';
  if (typeof v === 'number') return String(v);
  if (typeof v === 'boolean') return v ? '1' : '0';
  if (v instanceof Date) return `'${v.toISOString()}'`;
  if (Array.isArray(v)) return `'${JSON.stringify(v).replace(/'/g, "''")}'`;
  if (typeof v === 'object') return `'${JSON.stringify(v).replace(/'/g, "''")}'`;
  // String — escape single quotes by doubling, per SQL standard.
  return `'${String(v).replace(/'/g, "''")}'`;
}

function adapt(table, row) {
  const out = { ...row };
  for (const col of BOOL_COLS[table] ?? []) {
    if (col in out) out[col] = out[col] ? 1 : 0;
  }
  for (const col of ARRAY_JSON_COLS[table] ?? []) {
    if (col in out && Array.isArray(out[col])) out[col] = JSON.stringify(out[col]);
  }
  // Date columns: Neon driver returns Date objects for timestamptz. Stringify
  // them so escapeSqlLiteral takes the string branch.
  for (const [k, v] of Object.entries(out)) {
    if (v instanceof Date) out[k] = v.toISOString();
  }
  return out;
}

async function dumpTable(table) {
  const rows = await sql.query(`SELECT * FROM "${table}" ORDER BY 1`);
  if (rows.length === 0) {
    console.log(`  ${table}: 0 rows (skipped)`);
    return [];
  }
  const cols = Object.keys(rows[0]);
  const lines = [];
  lines.push(`-- ${table}: ${rows.length} row${rows.length === 1 ? '' : 's'}`);
  for (const raw of rows) {
    const r = adapt(table, raw);
    const values = cols.map((c) => escapeSqlLiteral(r[c])).join(', ');
    lines.push(
      `INSERT INTO ${table} (${cols.join(', ')}) VALUES (${values}) ON CONFLICT DO NOTHING;`,
    );
  }
  console.log(`  ${table}: ${rows.length} row${rows.length === 1 ? '' : 's'}`);
  return lines;
}

const outFile = 'migrations/d1/data.sql';
const apply = process.argv.includes('--apply');

console.log('Dumping Neon tables...');
const sections = [];
sections.push('-- Generated by scripts/migrate-neon-to-d1.mjs');
sections.push('-- Auto-applied via `wrangler d1 execute photoblog --remote --file=...`');
sections.push('-- Re-runnable: every INSERT carries ON CONFLICT DO NOTHING.');
sections.push('PRAGMA foreign_keys = OFF;');
for (const table of TABLE_ORDER) {
  const lines = await dumpTable(table);
  sections.push(...lines);
}
sections.push('PRAGMA foreign_keys = ON;');
writeFileSync(outFile, sections.join('\n') + '\n');
console.log(`\nWrote ${outFile}`);

if (!apply) {
  console.log('\nDry run. Pass --apply to push to D1 via wrangler.');
  process.exit(0);
}

console.log('\nApplying to remote D1...');
const out = execFileSync(
  './node_modules/.bin/wrangler',
  ['d1', 'execute', 'photoblog', '--remote', `--file=${outFile}`],
  { encoding: 'utf8' },
);
console.log(out.split('\n').slice(-20).join('\n'));
